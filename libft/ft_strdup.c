#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(s) + 1;
	dup = (char *)malloc(sizeof(char) * (size));
	if (dup == NULL)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
